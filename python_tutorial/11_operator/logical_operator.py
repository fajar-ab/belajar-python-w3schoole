# operator logika digunakan untuk
# menggabungkan pernyataan kondisional

x = 5

# jika suatu bilangan lebih besar dari 0 dan kurang dari 10
print(x > 0 and x < 10)

# jika suatu bilangan kurang dari 5 atau lebih besar dari 10
print(x < 5 or x > 10)

# mengembalikan hasilnya dengan not
print(not(x > 3 and x < 10))