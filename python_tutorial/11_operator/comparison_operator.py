# operasi perbandingan digunakan
# untuk membandingkan dua nilai

x = 5
y = 3

# equal
print(x == y)

# not equal
print(x != y)

# greater than
print(x > y)

# less than
print(x < y)

# greater than or equal to
print(x >= y)

# less than or qual to
print(x <= y)

# operator perbandingan rantai

x = 5

print(1 < x < 10)