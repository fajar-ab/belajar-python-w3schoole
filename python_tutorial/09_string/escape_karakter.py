# meyisipkan karakter yang ilegal dalam string,
# menggunakan karakter escape

print("we are the so-called \"viking\" from the north")

# single quote
print("it\'s alright")

# backslash
print("this will insert one \\ (backslash)")

# new line
print("hello\nworld")

# carriage return
print("hello\rworld")

# tab
print("hello\tworld")

# backspace
print("hello \bworld")

# octal value
print("\110\145\154\154\157")

# hex value
print("\x48\x65\x6c\x6c\x6f")