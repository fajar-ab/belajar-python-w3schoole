# banyak nilai untuk beberapa variable

x, y, z = "orange", "banana", "cerry"
print(x)
print(y)
print(z)

# satu nilai untuk beberapa variabel
x = y = z = "orange"
print(x)
print(y)
print(z)

# unpack a collection, mengekstrak nilai kedalam variabel

fruits = ["apple", "banana", "cerry"]
x, y, z = fruits
print(x)
print(y)
print(z)