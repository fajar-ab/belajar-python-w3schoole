# mendeklarasikan dan mengubah tipe variabel
x = 4       # x is of type int
x = "sally" # x is now of type str
print(x)

# casting menentukan type data

x = str(3)  # x will be '3'
print(x)

y = int(3)  # y will be 3
print(y)

z = float(3) # z will be 3.0
print(z)

