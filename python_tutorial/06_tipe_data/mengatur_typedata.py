x = str("hello world")
print(x) # display x
print(type(x)) # display the data type of x

x = int(20)
print(x) # display x
print(type(x)) # display the data type of x

x = float(20.5)
print(x) # display x
print(type(x)) # display the data type of x

x = complex(1j)
print(x) # display x
print(type(x)) # display the data type of x

x = list(("apple", "banana", "cherry"))
print(x) # display x
print(type(x)) # display the data type of x

x = tuple(("apple", "banana", "cherry"))
print(x) # display x
print(type(x)) # display the data type of x

x = range(10)
print(x) # display x
print(type(x)) # display the data type of x

x = dict(name="jhon", age=36)
print(x) # display x
print(type(x)) # display the data type of x

x = set(("apple", "banana", "cherry"))
print(x) # display x
print(type(x)) # display the data type of x

x = frozenset(("apple", "banana", "cherry"))
print(x) # display x
print(type(x)) # display the data type of x

x = bool(True)
print(x) # display x
print(type(x)) # display the data type of x

x = bytes(5)
print(x) # display x
print(type(x)) # display the data type of x

x = bytearray(5)
print(x) # display x
print(type(x)) # display the data type of x

x = memoryview(bytes(5))
print(x) # display x
print(type(x)) # display the data type of x


