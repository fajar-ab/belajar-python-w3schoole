# float
# bilangan pecahan angka positif atau negatif, 
# yang mengandung satu atau lebih desimal

x = 1.10
y = 1.0
z = -35.59

print(x)
print(type(x))

print(y)
print(type(y))

print(z)
print(type(z))

# float juga dapat berupa bilangan ilmiah
# engan "e" untuk menunjukkan pangkat 10

x = 35e3
y = 12E4
z = -87.7e100

print(x)
print(type(x))

print(y)
print(type(y))

print(z)
print(type(z))